def main():
    """批量生成样本 JSON 文件。"""
    # 清空已有示例文件（注意：只清理本脚本生成的占位文件）
    for dynasty_dir in OUT_DIR.iterdir():
        if dynasty_dir.is_dir():
            for f in dynasty_dir.glob("*.json"):
                # 只删除 content 是占位字符串的文件 — 仅本脚本可识别
                # 实际安全起见，这里直接覆盖所有（初次 build 允许）
                pass
    
    created = []
    for slug, name, dynasty, author, year, calligraphy, source, desc in SAMPLES:
        dynasty_dir_map = {
            "秦漢": "qin_han",
            "魏晉南北朝": "wei_jin_nanbei",
            "隋唐五代": "sui_tang",
            "宋遼金": "song_liao_jin",
            "元明清": "yuan_ming_qing",
            "近現代": "modern",
        }
        dir_name = dynasty_dir_map.get(dynasty)
        if not dir_name:
            print(f"[skip] 未知朝代: {dynasty}")
            continue
        target_dir = OUT_DIR / dir_name
        target_dir.mkdir(parents=True, exist_ok=True)
        out_file = target_dir / f"{slug}.json"
        
        obj = {
            "name": name,
            "dynasty": dynasty,
            "author": author,
            "year": year,
            "calligraphy_type": calligraphy,
            "source": source,
            "editor": "beixie-team",
            "release_date": "2024-12-01",
            "description": desc,
            "content": f"【{name}释文】此为占位文本，待依据 {source} 整理释文。原文据载：{name}，{author}书，{dynasty}刻立。",
        }
        with out_file.open("w", encoding="utf-8") as f:
            json.dump(obj, f, ensure_ascii=False, indent=2)
            f.write("\n")
        created.append(out_file.relative_to(ROOT).as_posix())
    
    print(f"已生成 {len(created)} 个样本文件。")
    for p in created:
        print(f"  ✓ {p}")


if __name__ == "__main__":
    main()
